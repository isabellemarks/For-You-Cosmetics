    private void alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarActionPerformed

        EnderecoDao endereco = new EnderecoDao();
        ClienteDao client = new ClienteDao();
        TelefoneDao telefone = new TelefoneDao();
        TratamentoDeErros tratamentoDeErros = new TratamentoDeErros();

        Endereco meuEndereco = new Endereco(this.campoEndereco.getText(), this.campoBairro.getText(), Integer.parseInt(this.campoCep.getText()), this.campoComplemento.getText(), this.campoNumero.getText());
        int idEndereco = client.consultarporIdParaAtualizarEndereco(Integer.parseInt(this.id.getText()));
        meuEndereco.setIdEndereco(idEndereco);
        endereco.alterarUmEndereco(meuEndereco);
        tratamentoDeErros.TestarExecao(idEndereco);

        if (idEndereco > 0) {
            Telefone meuTelefone = new Telefone(Integer.parseInt(this.campoTelefone.getText()));
            int idTelefone = telefone.consultarporIdParaAtualizarTelefone(Integer.parseInt(this.id.getText()));
            meuTelefone.setIdTelefone(idTelefone);
            telefone.alterarUmTelefone(meuTelefone);
            tratamentoDeErros.TestarExecao(idTelefone);

            if (idTelefone > 0) {
                java.util.Date DataInformada = new java.util.Date(Integer.parseInt(this.ano.getSelectedItem().toString()) - 1900, this.mes.getSelectedIndex(), Integer.parseInt(this.data.getSelectedItem().toString()));
                java.sql.Date dataFinal = new java.sql.Date(DataInformada.getTime());

                Cliente meuCliente = new Cliente(this.campoNome.getText(), dataFinal, this.campoEmail.getText(), meuEndereco, Arrays.asList(meuTelefone));
                int idCli = client.consultarporIdParaAtualizarCliente(Integer.parseInt(this.id.getText()));
                meuCliente.setIdCliente(idCli);
                client.alterarUmCliente(meuCliente);
                tratamentoDeErros.TestarExecao(idCli);

                if (idCli > 0) {

                    JOptionPane.showMessageDialog(this, "Cliente alterado com sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Falha ao alterar o Cliente", "Sucesso", JOptionPane.ERROR_MESSAGE);
                }

            }
        }

    }//GEN-LAST:event_alterarActionPerformed

