    public void carregarCamposTela() {
        this.id.setText(String.valueOf(this.cliente.getIdCliente()));
        this.campoNome.setText(this.cliente.getNome());
        this.campoEmail.setText(this.cliente.getEmail());
        this.campoEndereco.setText(this.cliente.getEndereco().getRua());
        this.campoCep.setText(String.valueOf(this.cliente.getEndereco().getCep()));
        this.campoNumero.setText(this.cliente.getEndereco().getNumero());
        this.campoBairro.setText(this.cliente.getEndereco().getBairro());
        this.campoComplemento.setText(String.valueOf(this.cliente.getEndereco().getComplemento()));
        this.campoTelefone.setText(this.cliente.getTelefones().get(0).toString());
        Date dataCliente = cliente.getDataNascimento();
        int dia = dataCliente.getDay();
        int mes = dataCliente.getMonth();
        int ano = dataCliente.getYear();
        this.data.setSelectedItem(dia);
        this.mes.setSelectedIndex(mes);
        this.ano.setSelectedItem(ano);

    }

