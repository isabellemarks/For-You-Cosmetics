    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        FornecedorDao preencherCombo = new FornecedorDao();
        ProdutoDao cadastrarProduto = new ProdutoDao();
        TratamentoDeErros tratamentoDeErros = new TratamentoDeErros();

        Fornecedor meuFornecedor = new Fornecedor(this.fornecedores.getSelectedItem().toString());

        meuFornecedor.setIdFornecedor(preencherCombo.preencheComboBox(meuFornecedor));

        int radio = 0;
        if (ativo.isSelected()) {
            radio = 1;

            if (naoAtivo.isSelected()) {
                radio = 0;
            }
        }

        Produto meuProduto = new Produto(this.campoNomedoProduto.getText(), this.CampoDescricao.getText(), Float.parseFloat(this.campoPreco.getText()), radio, meuFornecedor);
        int idProduto = cadastrarProduto.cadastrarUmProduto(meuProduto);
        tratamentoDeErros.TestarExecao(idProduto);
        if (idProduto > 0) {
            meuProduto.setIdProduto(idProduto);
            if (idProduto > 0) {
                JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Falha ao cadastrar o Produto", "Error", JOptionPane.ERROR_MESSAGE);

            }
            limparTela();

        }


    }//GEN-LAST:event_cadastrarActionPerformed

