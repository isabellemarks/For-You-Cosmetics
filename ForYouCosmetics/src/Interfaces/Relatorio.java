        int mes = (this.mesCombo.getSelectedIndex()+1);
        int ano = Integer.parseInt(this.anoCombo.getSelectedItem().toString());
        //System.out.println(mes);
        //System.out.println(ano);
        resultados = relatorio.gerarRelatorio(mes, ano);

        for (Relatorios umRelatorio : resultados) {
            modelo.addRow(new String[]{umRelatorio.getItensdopedido().getProduto().getNomeProduto(), String.valueOf(umRelatorio.getIdPedido().getValorPedidoTotal()),
                String.valueOf(umRelatorio.getItensdopedido().getQuantidade())});
        }

