    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       ClienteDao preencherCombo = new ClienteDao();
        PedidoDao cadastrarPedido = new PedidoDao();

        Cliente meuCliente = new Cliente(this.nomesDosClientes.getSelectedItem().toString());
        meuCliente.setIdCliente(preencherCombo.preencheComboBox(meuCliente));
        
        java.util.Date DataInformada = new java.util.Date(Integer.parseInt(this.anoPedido.getSelectedItem().toString()) - 1900, this.mesPedido.getSelectedIndex(), Integer.parseInt(this.diaPedido.getSelectedItem().toString()));
        java.sql.Date dataFinal = new java.sql.Date(DataInformada.getTime());
        Pedido meuPedido = new Pedido(Float.parseFloat(this.campoValor.getText()), dataFinal, meuCliente);
        int idPedido = cadastrarPedido.cadastrarUmPedido(meuPedido);
        if (idPedido > 0) {
            JOptionPane.showMessageDialog(this, "Pedido cadastrado com sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Falha ao cadastrar o Pedido", "Error", JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_jButton2ActionPerformed

